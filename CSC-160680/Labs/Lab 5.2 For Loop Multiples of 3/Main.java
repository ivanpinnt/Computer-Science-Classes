// Write a program that uses a FOR loop to print the multiples of 3 from 3 through 99

//

class Main
{
    public static void main(String[] args)
    {

        System.out.println("Multiples of Three");

        for (int i=3; i<=99;i+=3){
            System.out.println(i);
        }

    }
}