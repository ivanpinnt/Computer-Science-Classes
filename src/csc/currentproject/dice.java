package csc.currentproject;

import java.util.Random;

// DICE ROLL
public class dice
{
    ////////////////////////////////////////////////////////////////////////////////////////
    // DICE ROLL
    ////////////////////////////////////////////////////////////////////////////////////////
    // Total sides of die
    private final static int totalSides = 6;

    // Method of rolling random numbers, from the die
    int rollDice()
    {
        int finalRoll;
        Random random = new Random();
        finalRoll = random.nextInt(totalSides) + 1;
        return finalRoll;
    }

    ////////////////////////////////////////////////////////////////////////////////////////
    // PLAYER STORAGE
    ////////////////////////////////////////////////////////////////////////////////////////
    // SET
    // Player 1 Storage
    int p1;
    void p1Total(int total){
        p1 += total;
    }
    // Player 2 Storage
    int p2;
    void p2Total(int total){
        p2 += total;
    }

    // GET
    int getP1Total(){return p1;}
    int getP2Total(){return p2;}

    ////////////////////////////////////////////////////////////////////////////////////////
    // PLAYER STORAGE
    ////////////////////////////////////////////////////////////////////////////////////////
}